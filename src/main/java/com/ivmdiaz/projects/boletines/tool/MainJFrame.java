    private void btnEnviarBoletinesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarBoletinesActionPerformed

        final List<Directorio> directorioList = this.panelDirectorio.getDirectorioList();
        final ConfiguracionCorreo configuracion = this.panelConfiguracion.getConfiguracionCorreo();
        if (!CollectionUtils.isEmpty(directorioList)) {
            this.panelEnviarCorreos = new PanelEnviarBoletines(directorioList, 
                    configuracion);
            this.actualizarContenido(this.panelEnviarCorreos);
        }
        else {
            JOptionPane.showMessageDialog(this, "Error. Debe cargar un directorio", "Error", 0);
        }

    }//GEN-LAST:event_btnEnviarBoletinesActionPerformed

