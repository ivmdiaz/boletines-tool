    public ConfiguracionCorreo getConfiguracionCorreo() {
        
        final ConfiguracionCorreo configuracionCorreo = new ConfiguracionCorreo();
        configuracionCorreo.setUsuario(this.txtUsuario.getText());
        configuracionCorreo.setClave(String.valueOf(this.txtClave.getPassword()));
        configuracionCorreo.setHost(this.txtHost.getText());
        configuracionCorreo.setPuerto(this.txtPuerto.getText());
        configuracionCorreo.setAuth(this.checkAuthEnable.isSelected() ? "true": "false");
        configuracionCorreo.setTls(this.checkStartTls.isSelected() ? "true": "false");
        
        return configuracionCorreo;
    }

